public class Donas {
    private int masa;
    private String ingrediente;
    private int vainilla;
    private int leche;
    private int huevos;
    private int cantidadingrediente;

    public int getMasa() {
        return masa;
    }

    public void setMasa(int masa) {
        this.masa = masa;
    }

    public String getIngrediente() {
        return ingrediente;
    }

    public void setIngrediente(String ingrediente) {
        this.ingrediente = ingrediente;
    }

    public int getVainilla() {
        return vainilla;
    }

    public void setVainilla(int vainilla) {
        this.vainilla = vainilla;
    }

    public int getLeche() {
        return leche;
    }

    public void setLeche(int leche) {
        this.leche = leche;
    }

    public int getHuevos() {
        return huevos;
    }

    public void setHuevos(int huevos) {
        this.huevos = huevos;
    }

    public int getCantidadingrediente() {
        return cantidadingrediente;
    }

    public void setCantidadingrediente(int cantidadingrediente) {
        this.cantidadingrediente = cantidadingrediente;
    }
    
    
}
